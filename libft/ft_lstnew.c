#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*skrt;

	skrt = (t_list *)malloc(sizeof(t_list));
	if (skrt)
	{
		skrt->content = content;
		skrt->next = 0;
	}
	return (skrt);
}
