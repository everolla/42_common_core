#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		z;
	char	*s2;

	i = 0;
	while (s1[i])
		i++;
	s2 = malloc((i + 1) * sizeof(char));
	if (!s2)
		return (NULL);
	z = 0;
	while (z <= i)
	{
		s2[z] = s1[z];
		z++;
	}
	return (s2);
}
